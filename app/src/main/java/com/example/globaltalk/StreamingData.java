package com.example.globaltalk;

public class StreamingData {


    private String streaming_no;

    private String streaming_thumbnail;
    private String stream_name;
    private String room_name;



    public String getstreaming_no() {
        return streaming_no;
    }

    public String getstreaming_thumbnail() {
        return streaming_thumbnail;
    }

    public String getstream_name() {
        return stream_name;
    }

    public String getroom_name() {
        return room_name;
    }



    public void setstreaming_no(String streaming_no) {
        this.streaming_no = streaming_no;
    }

    public void setstreaming_thumbnail(String streaming_thumbnail) {
        this.streaming_thumbnail = streaming_thumbnail;
    }

    public void setstream_name(String stream_name) {
        this.stream_name = stream_name;
    }

    public void setroom_name(String room_name) {
        this.room_name = room_name;
    }





}
