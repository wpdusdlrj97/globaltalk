package com.example.globaltalk;

public class NewsData {


    private String news_thumbnail;
    private String news_title;
    private String news_subtitle;
    private String news_writer;

    private String news_link;
    private String news_content;



    public String getNews_link() {
        return news_link;
    }

    public void setNews_link(String news_link) {
        this.news_link = news_link;
    }


    public String getNews_content() {
        return news_content;
    }

    public void setNews_content(String news_content) {
        this.news_content = news_content;
    }


    public String getNews_thumbnail() {
        return news_thumbnail;
    }

    public void setNews_thumbnail(String news_thumbnail) {
        this.news_thumbnail = news_thumbnail;
    }



    public String getNews_title() {
        return news_title;
    }

    public void setNews_title(String news_title) {
        this.news_title = news_title;
    }

    public String getNews_subtitle() {
        return news_subtitle;
    }

    public void setNews_subtitle(String news_subtitle) {
        this.news_subtitle = news_subtitle;
    }


    public String getNews_writer() {
        return news_writer;
    }

    public void setNews_writer(String news_writer) {
        this.news_writer = news_writer;
    }









}
