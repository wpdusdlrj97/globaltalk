package com.example.globaltalk;

public class PersonalData {
    private String member_email;
    private String member_image;
    private String member_name;
    private String member_teach;
    private String member_learn;

    public String getMember_email() {
        return member_email;
    }

    public String getMember_image() {
        return member_image;
    }

    public String getMember_name() {
        return member_name;
    }

    public String getMember_teach() {
        return member_teach;
    }

    public String getMember_learn() {
        return member_learn;
    }

    public void setMember_email(String member_email) {
        this.member_email = member_email;
    }

    public void setMember_image(String member_image) {
        this.member_image = member_image;
    }

    public void setMember_name(String member_name) {
        this.member_name = member_name;
    }

    public void setMember_teach(String member_teach) {
        this.member_teach = member_teach;
    }

    public void setMember_learn(String member_learn) {
        this.member_learn = member_learn;
    }
}
